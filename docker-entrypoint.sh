#!/bin/sh
# Точка входа прод-контейнера: применяем миграции, затем запускаем сервер.
# `migrate deploy` создаёт/обновляет схему БД и НЕ трогает данные.
set -e

echo "[Stell22] Применяю миграции базы данных..."
npx prisma migrate deploy

echo "[Stell22] Запускаю приложение на порту ${PORT:-3000}..."
exec npm run start
